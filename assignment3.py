"""function to Convert Celsius to Fahrenheit"""

a = float(input("Enter a temperature value: "))

def cels_to_fahr(celsus):
    bresult = (celsus * 1.8) + 32
    return bresult

fahr = cels_to_fahr(a)
print(f"Your Celsius {a:.2f} is equivalent to Fahrenheit {fahr:.2f}")







